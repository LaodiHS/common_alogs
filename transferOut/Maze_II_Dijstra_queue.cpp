    int shortestDistance(vector<vector<int>>& maze, vector<int>& start, vector<int>& destination) {//19
        int m = maze.size(), n = maze[0].size();
        vector<vector<int>> distance(m,vector<int>(n,-1));
        queue<pair<int,int>> myq;
        distance[ start[0]] [start[1] ] = 0;
        myq.emplace(start[0],start[1]);
        int steps=0;
        int dir[][2]={{0,1},{1,0},{0,-1},{-1,0}};
        while(!myq.empty()){
            auto x = myq.front(); myq.pop();
            int r=x.first,c=x.second;
            for(auto d:dir){
                steps=0;
                int rr=r,cc=c;
                while(rr+d[0] >= 0 && rr + d[0] < m && cc + d[1] >= 0 && cc + d[1] < n && maze[rr + d[0]][cc + d[1]] != 1){
                    steps++; rr = rr + d[0]; cc = cc + d[1];
                   };
                if(distance[rr][cc] == -1 || distance[rr][cc] > distance[r][c] + steps){
                    distance[rr][cc] = distance[r][c] + steps;
                    myq.emplace(rr,cc);//new source point the potenial shortest path
                                }
                
                            }
                           }
        return distance[destination[0]][destination[1]];
    
 } 

